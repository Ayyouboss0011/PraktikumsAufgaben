package Aufgabenteil1;
/*
das Ergebnis der Division der als int-Variablen deklarierten ganzen Zahlen 14 und 2 (14 geteilt durch 2) berechnet,
in einer weiteren Variablen vom Typ int speichert und dann auf der Konsole ausgibt.
*/
public class Aufgabe3 {
    public static void main(String[] args) {
        int Zahl1  = 14;
        int Zahl2 = 2;

        int ergebnis = Zahl1 / Zahl2;
        System.out.println(ergebnis);
    }
}
